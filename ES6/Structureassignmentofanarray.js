// In JavaScript, destructuring assignment is a convenient way of extracting values from arrays (or properties from objects) and assigning them to variables. This simplifies the process of extracting elements and improves readability.

// Destructuring Assignment for Arrays
// The syntax for destructuring an array looks like this:


// let [variable1, variable2, ...] = array;
// Simple Example of Array Destructuring:

// An array with three elements
let numbers = [10, 20, 30];

// Destructuring assignment
let [first, second, third] = numbers;

console.log(first);  // Output: 10
console.log(second); // Output: 20
console.log(third);  // Output: 30