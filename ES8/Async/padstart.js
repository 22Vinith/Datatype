// padStart() 

// Syntax:
// string.padStart(targetLength, padString)

// Example:
let str = "5";
let paddedStart = str.padStart(3, "0");

console.log(paddedStart); // Output: "05"
// Explanation:
// Here, "5" is padded with "0" from the start to make it 3 characters long. As a result, the output is "05".